import { NextPage } from "next";
import { useState } from "react";
import {
  Container,
  Grid,
  SimpleGrid,
  Skeleton,
  useMantineTheme,
  rem,
} from "@mantine/core";

const PRIMARY_COL_HEIGHT = rem(300);

export function LeadGrid() {
  const theme = useMantineTheme();
  const SECONDARY_COL_HEIGHT = `calc(${PRIMARY_COL_HEIGHT} / 2 - ${theme.spacing.md} / 2)`;

  return (
    <Container my="md">
      <SimpleGrid
        cols={2}
        spacing="md"
        breakpoints={[{ maxWidth: "sm", cols: 1 }]}
      >
        <Skeleton height={PRIMARY_COL_HEIGHT} radius="md" animate={false} />
        <Grid gutter="md">
          <Grid.Col>
            <Skeleton
              height={SECONDARY_COL_HEIGHT}
              radius="md"
              animate={false}
            />
          </Grid.Col>
          <Grid.Col span={6}>
            <Skeleton
              height={SECONDARY_COL_HEIGHT}
              radius="md"
              animate={false}
            />
          </Grid.Col>
          <Grid.Col span={6}>
            <Skeleton
              height={SECONDARY_COL_HEIGHT}
              radius="md"
              animate={false}
            />
          </Grid.Col>
        </Grid>
      </SimpleGrid>
    </Container>
  );
}

const Home: NextPage = () => {
  const [weight, setWeight] = useState(0);
  const [height, setHeight] = useState(0);
  const [bmi, setBmi] = useState<number>();

  const getBMI = () => {
    const heightInMeter = height / 100;
    const bmi: number = weight / heightInMeter ** 2;
    setBmi(Number(bmi.toFixed(2)));
  };

  return (
    <main className="container mx-auto">
      <div className="flex min-h-screen w-full items-center justify-center bg-gray-100">
        <div className="w-96 rounded-lg bg-white p-8 shadow-md">
          <h2 className="mb-6 text-2xl font-semibold">BMI Calculator</h2>
          <div className="mb-4">
            <label htmlFor="weight" className="block text-gray-700">
              体重 (kg)
            </label>
            <input
              id="weight"
              type="number"
              value={weight}
              className="mt-2 w-full rounded border border-gray-300 p-2"
              //入力が変化したら状態に反映させる
              onChange={(event) => setWeight(event.target.valueAsNumber)}
            />
          </div>
          <div className="mb-6">
            <label htmlFor="height" className="block text-gray-700">
              身長 (cm)
            </label>
            <input
              id="height"
              type="number"
              value={height}
              className="mt-2 w-full rounded border border-gray-300 p-2"
              //入力が変化したら状態に反映させる
              onChange={(event) => setHeight(event.target.valueAsNumber)}
            />
          </div>
          <button
            className="rounded bg-blue-500 px-4 py-2 text-white hover:bg-blue-600"
            onClick={getBMI} //event
          >
            BMI を計算する
          </button>

          <div>{bmi}</div>
        </div>
      </div>
    </main>
  );
};

export default Home;
